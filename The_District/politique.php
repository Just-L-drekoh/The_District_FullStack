Nous attachons une grande importance à la confidentialité de vos informations. Cette politique de confidentialité décrit les types d'informations personnelles que nous collectons et recevons lorsque vous utilisez notre site Web, comment nous utilisons ces informations, et les mesures que nous prenons pour les protéger.
</p>
<h6>
Collecte et utilisation des informations
</h6>
<h3>
Informations que nous collectons
</h3>
<p>
Lorsque vous utilisez notre site Web, nous pouvons collecter les types d'informations suivants :

Informations personnelles identifiables : telles que votre nom, adresse e-mail, numéro de téléphone, et adresse postale, que vous pouvez fournir volontairement lors de la création d'un compte, de la passation d'une commande ou de la communication avec notre service client.

Informations d'utilisation : telles que votre adresse IP, votre navigateur Web, votre système d'exploitation, et les pages que vous consultez sur notre site Web, collectées automatiquement à des fins d'analyse et d'amélioration de l'expérience utilisateur.
</p>
<h6>
Utilisation des informations
</h6>
<h3>
Nous utilisons les informations collectées pour :
</h3>
<p>
Fournir, personnaliser et améliorer nos services.
Traiter et livrer vos commandes.
Communiquer avec vous concernant votre compte et vos commandes.
Répondre à vos demandes et questions.
Vous envoyer des informations commerciales et promotionnelles, conformément à vos préférences de communication.
Partage des informations
Nous ne vendons pas, ne louons pas et ne divulguons pas vos informations personnelles à des tiers, sauf dans les cas suivants :

Avec votre consentement explicite.
Pour répondre aux exigences légales ou réglementaires.
Pour protéger nos droits, notre propriété ou notre sécurité, ainsi que ceux de nos utilisateurs ou du public.
Sécurité des informations
Nous mettons en œuvre des mesures de sécurité appropriées pour protéger vos informations personnelles contre tout accès non autorisé, toute altération, toute divulgation ou toute destruction.
</p>
<h3>
Vos choix
</h3>
<p>
Vous pouvez choisir de ne pas fournir certaines informations personnelles, mais cela peut vous empêcher d'accéder à certaines fonctionnalités ou services de notre site Web.

Vous pouvez également choisir de ne pas recevoir de communications commerciales de notre part en ajustant vos préférences de communication dans votre compte utilisateur ou en nous contactant directement.

Modifications de cette politique
Nous nous réservons le droit de modifier cette politique de confidentialité à tout moment. Toute modification sera effective dès sa publication sur cette page. Nous vous encourageons à consulter régulièrement cette page pour rester informé des mises à jour.

Contactez-nous
Si vous avez des questions ou des préoccupations concernant cette politique de confidentialité, veuillez nous contacter à l'adresse suivante : TheDistrict@mail.com.
